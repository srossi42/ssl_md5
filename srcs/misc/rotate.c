#include "../../includes/ft_ssl.h"

uint32_t	ft_rotate_left(uint32_t x, uint32_t n)
{
	return ((x << n) | (x >> (32 - n)));
}

uint32_t	ft_rotate_right(uint32_t x, uint32_t n)
{
	return ((x >> n) | (x << (32 - n)));
}
